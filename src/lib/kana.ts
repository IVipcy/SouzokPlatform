// カナ正規化ユーティリティ。
// 銀行CSVの振込人（半角カナ）と、依頼者のふりがな（ひらがな）/振込名義人を
// 同じ土俵で比較・自動入力するために、すべて「全角カタカナ」に寄せる。

// 半角カタカナ → 全角カタカナ（濁点・半濁点は後段で合成）
const HALF_TO_FULL: Record<string, string> = {
  'ｱ': 'ア', 'ｲ': 'イ', 'ｳ': 'ウ', 'ｴ': 'エ', 'ｵ': 'オ',
  'ｶ': 'カ', 'ｷ': 'キ', 'ｸ': 'ク', 'ｹ': 'ケ', 'ｺ': 'コ',
  'ｻ': 'サ', 'ｼ': 'シ', 'ｽ': 'ス', 'ｾ': 'セ', 'ｿ': 'ソ',
  'ﾀ': 'タ', 'ﾁ': 'チ', 'ﾂ': 'ツ', 'ﾃ': 'テ', 'ﾄ': 'ト',
  'ﾅ': 'ナ', 'ﾆ': 'ニ', 'ﾇ': 'ヌ', 'ﾈ': 'ネ', 'ﾉ': 'ノ',
  'ﾊ': 'ハ', 'ﾋ': 'ヒ', 'ﾌ': 'フ', 'ﾍ': 'ヘ', 'ﾎ': 'ホ',
  'ﾏ': 'マ', 'ﾐ': 'ミ', 'ﾑ': 'ム', 'ﾒ': 'メ', 'ﾓ': 'モ',
  'ﾔ': 'ヤ', 'ﾕ': 'ユ', 'ﾖ': 'ヨ',
  'ﾗ': 'ラ', 'ﾘ': 'リ', 'ﾙ': 'ル', 'ﾚ': 'レ', 'ﾛ': 'ロ',
  'ﾜ': 'ワ', 'ｦ': 'ヲ', 'ﾝ': 'ン',
  'ｧ': 'ァ', 'ｨ': 'ィ', 'ｩ': 'ゥ', 'ｪ': 'ェ', 'ｫ': 'ォ',
  'ｯ': 'ッ', 'ｬ': 'ャ', 'ｭ': 'ュ', 'ｮ': 'ョ', 'ｰ': 'ー',
}
// 濁点が付く全角カタカナ
const DAKUTEN: Record<string, string> = {
  'カ': 'ガ', 'キ': 'ギ', 'ク': 'グ', 'ケ': 'ゲ', 'コ': 'ゴ',
  'サ': 'ザ', 'シ': 'ジ', 'ス': 'ズ', 'セ': 'ゼ', 'ソ': 'ゾ',
  'タ': 'ダ', 'チ': 'ヂ', 'ツ': 'ヅ', 'テ': 'デ', 'ト': 'ド',
  'ハ': 'バ', 'ヒ': 'ビ', 'フ': 'ブ', 'ヘ': 'ベ', 'ホ': 'ボ',
  'ウ': 'ヴ',
}
// 半濁点が付く全角カタカナ
const HANDAKUTEN: Record<string, string> = {
  'ハ': 'パ', 'ヒ': 'ピ', 'フ': 'プ', 'ヘ': 'ペ', 'ホ': 'ポ',
}

/** ひらがな・半角カタカナを全角カタカナへ変換する（濁点・半濁点を合成） */
export function toKatakana(input: string | null | undefined): string {
  const s = input ?? ''
  let out = ''
  for (let i = 0; i < s.length; i++) {
    let c = s[i]
    const code = c.charCodeAt(0)
    // ひらがな → カタカナ
    if (code >= 0x3041 && code <= 0x3096) c = String.fromCharCode(code + 0x60)
    // 半角カタカナ → 全角
    else if (HALF_TO_FULL[c]) c = HALF_TO_FULL[c]
    // 次が濁点/半濁点なら合成（半角ﾞﾟ・全角゛゜・結合文字に対応）
    const next = s[i + 1]
    if ((next === 'ﾞ' || next === '゛' || next === '゙') && DAKUTEN[c]) { out += DAKUTEN[c]; i++; continue }
    if ((next === 'ﾟ' || next === '゜' || next === '゚') && HANDAKUTEN[c]) { out += HANDAKUTEN[c]; i++; continue }
    out += c
  }
  return out
}

/** カナ比較用の正規化：全角カタカナ化＋空白除去（前方/部分一致の土台） */
export function kanaKey(input: string | null | undefined): string {
  return toKatakana(input).replace(/[\s　]/g, '')
}
