-- 036_invoice_status_draft.sql
-- 請求書のステータスに '作成済' を追加。
--
-- これまでは「請求書を発行（作成）した瞬間」に
-- 確定請求済 / 前受金請求済 になっていたが、実務では
--   作成 → お客様に送付 → 「確定請求済」
-- のステップなので、作成直後はまだ未送付の状態として
-- '作成済' を持てるようにする。
-- ============================================================

-- CHECK 制約を更新（旧制約を削除して再作成）
ALTER TABLE invoices DROP CONSTRAINT IF EXISTS invoices_status_check;

ALTER TABLE invoices
  ADD CONSTRAINT invoices_status_check CHECK (
    status IN ('未請求', '作成済', '前受金請求済', '前受金入金済', '確定請求済', '入金済', '一部入金')
  );
