-- ============================================================
-- 160_funeral_route_split.sql
-- 面談ルート「葬儀社経由」を「主要取引先葬儀社／その他葬儀社」に分割したことに伴う既存データ移行。
-- 紹介元名が主要4社（はせがわ/公益社/伊藤典範/横浜セレモ）なら主要取引先葬儀社、それ以外はその他葬儀社へ。
-- 案件番号の経路コードは両ルートとも SD を共用（集計は order_route 列で区別）。
-- ============================================================

-- 1) 主要4社に一致するものは「主要取引先葬儀社」へ
UPDATE cases
  SET order_route = '主要取引先葬儀社'
  WHERE order_route = '葬儀社経由'
    AND order_route_detail IN ('はせがわ', '公益社', '伊藤典範', '横浜セレモ');

-- 2) 残りの旧「葬儀社経由」は「その他葬儀社」へ
UPDATE cases
  SET order_route = 'その他葬儀社'
  WHERE order_route = '葬儀社経由';
