-- ============================================================
-- 061_case_status_remove_new.sql
-- 「新規（架電案件化）」ステータスの廃止と分類の整理
--
--   1. 既存の status='架電案件化'（新規）の案件はすべて '面談設定済' へ移行。
--      （案件は必ず「面談設定済」＝相談案件から開始する運用に統一）
--   2. status_definitions マスタ:
--        - 架電案件化（新規）の定義を削除
--        - '保留・長期' の表示ラベルを「長期保留」に変更（内部キーは維持）
--
-- 注: cases.status は CHECK 制約を持たない TEXT 列のため、値の移行とマスタ更新で足りる。
--
-- 案件分類（参考）:
--   相談案件     = 面談設定済 / 検討中 / 検討中（契約書待ち） / 受注（受託） / 失注（不受託）
--   個別管理案件 = 紹介のみ / 保留・長期（長期保留）
--   管理案件     = 対応中 / 完了
-- ============================================================

-- 1. 既存「新規」案件を「面談設定済」へ移行
UPDATE cases SET status = '面談設定済' WHERE status = '架電案件化';

-- 2-a. 新規（架電案件化）のステータス定義を削除
DELETE FROM status_definitions WHERE type = 'case' AND key = '架電案件化';

-- 2-b. 「保留・長期」の表示ラベルを「長期保留」に変更（内部キーは維持）
UPDATE status_definitions SET label = '長期保留' WHERE type = 'case' AND key = '保留・長期';

-- 3. 「架電案件化」起点／終点の遷移ルールを削除（存在すれば）
DELETE FROM status_transitions WHERE type = 'case' AND (from_status = '架電案件化' OR to_status = '架電案件化');
