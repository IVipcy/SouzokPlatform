-- ============================================================
-- 081_deal_name_strip_suffix.sql
-- 案件名(deal_name)の自動付与サフィックス「 様 相続案件」を除去し、
-- 依頼人氏名のみにする（既存データの整理）。
-- 末尾が完全一致するものだけを対象にするため安全。
-- ============================================================

UPDATE cases
   SET deal_name = regexp_replace(deal_name, ' 様 相続案件$', '')
 WHERE deal_name LIKE '% 様 相続案件';
