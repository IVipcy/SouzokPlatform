-- =========================================================
-- 014: 残高証明タスクの統合
-- bank_balance_request に到着日管理を統合したため
-- bank_balance_arrive テンプレートを非表示化
-- =========================================================

-- bank_balance_arrive テンプレートを非アクティブ化
UPDATE task_templates
SET is_active = false
WHERE key = 'bank_balance_arrive';

-- bank_balance_request の手順テキストを更新（到着管理まで含む内容に）
UPDATE task_templates
SET procedure_text = '【作業内容】
全金融機関に残高証明書を請求し、書類が揃うまで管理します。

【手順】
□ タスク詳細の「残高証明請求（預貯金）」に金融機関一覧が表示されていることを確認する
□ 各金融機関の相続センターに電話し「相続手続きの書類を送付してほしい」と依頼する
□ 書類が届いたら、残高証明請求書・委任状・印鑑証明書・法定相続情報を準備する
□ 原本一式と一緒に郵送し、「請求日」を入力する
□ 書類が到着したら各金融機関の「到着日」を入力する
□ 全金融機関の到着日が入力できたらタスクを「完了」にする

【ポイント】
・通帳がある場合は一緒に送付すること。
・「相続税申告あり」の案件は相続開始日時点の残高も必要（管理担当に確認）。
・口座が凍結されていない場合は凍結依頼も同時に行い、「凍結済」にチェックを入れる。
・金融機関ごとに必要書類が異なる場合があるため、事前確認が必要。'
WHERE key = 'bank_balance_request';
