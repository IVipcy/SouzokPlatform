-- 到着物（受信簿アイテム）の「タスク紐づけ不要」フラグ。
-- 契約時受領書類（契約書・委任状・本人確認書類・印鑑証明書など）はタスク紐づけが不要なのに
-- 未紐づけとして催促されてしまうため、紐づけ不要を明示できるようにする。
--   NULL  = 既定（契約書類でタスク不要カテゴリなら自動で紐づけ不要扱い、それ以外は紐づけ必要）
--   TRUE  = 手動で「紐づけ不要」に設定
--   FALSE = 手動で「紐づけ必要」に設定（自動判定の上書き）
ALTER TABLE document_receipt_items
  ADD COLUMN IF NOT EXISTS link_not_required boolean;
