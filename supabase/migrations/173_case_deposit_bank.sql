-- 入金銀行は「お客さんがどちらの口座に振り込むか」で決まる（チーム固定のマスタではない）。
-- 案件ごとに入金銀行を保持し、確定売上表のシート分け（第一=みずほ／第二=きらぼし）に使う。
--   ・CSV突合で実入金の銀行を自動記録
--   ・未入金は経理が手動で振り分け（選択）
-- 172 で追加した teams.division / teams.bank は不使用（残置・無害）。
-- ============================================================
ALTER TABLE cases
  ADD COLUMN IF NOT EXISTS bank text;   -- 入金銀行（みずほ / きらぼし 等）

CREATE INDEX IF NOT EXISTS idx_cases_bank ON cases(bank);
