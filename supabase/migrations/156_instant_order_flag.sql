-- ============================================================
-- 156_instant_order_flag.sql
-- 「即受注」バッジ用フラグ。面談登録で面談結果=受注にした案件（＝面談設定済をその場で受注）を識別する。
-- ステータスは「受注」に統一し、その場受注かどうかは本フラグで表現（画面では受注ステータスに「即受注」バッジ）。
--   ・面談登録で受注を選択 → true（その場受注＝即受注）
--   ・依頼確定待ち→受注、戻り受注 などフラグ未設定は false（通常の受注）
-- ============================================================

ALTER TABLE cases
  ADD COLUMN IF NOT EXISTS instant_order boolean NOT NULL DEFAULT false;

COMMENT ON COLUMN cases.instant_order IS '即受注フラグ（面談登録で面談結果=受注にした＝その場受注のとき true）';
