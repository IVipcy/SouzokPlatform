-- 既に生成済みのタスクにも procedure_text を反映
UPDATE tasks t SET procedure_text = tt.procedure_text
FROM task_templates tt
WHERE t.template_key = tt.key
AND t.procedure_text IS NULL
AND tt.procedure_text IS NOT NULL;