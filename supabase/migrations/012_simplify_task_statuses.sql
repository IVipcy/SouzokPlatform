-- 012: タスクステータスを3段階に簡略化
-- 着手前 / 対応中 / 完了
-- =========================================================

-- 旧ステータスを新ステータスに変換
UPDATE tasks SET status = '着手前' WHERE status = '未着手';
UPDATE tasks SET status = '対応中' WHERE status IN ('Wチェック待ち', '差戻し', '保留');
UPDATE tasks SET status = '完了' WHERE status = 'キャンセル';
